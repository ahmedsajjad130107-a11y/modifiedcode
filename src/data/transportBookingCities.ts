const normalizeCity = (city: string): string =>
  city.replace(/\s+/g, ' ').trim().toUpperCase();

const toUniqueList = (raw: string): string[] => {
  const seen = new Set<string>();
  const list: string[] = [];
  raw
    .split('\n')
    .map((item) => item.trim())
    .filter(Boolean)
    .forEach((city) => {
      const key = normalizeCity(city);
      if (!seen.has(key)) {
        seen.add(key);
        list.push(city);
      }
    });
  return list;
};

export const TRAIN_CITIES = toUniqueList(TRAIN_CITIES_RAW);
export const BUS_CITIES = toUniqueList(BUS_CITIES_RAW);

export const isCityInList = (city: string, list: string[]): boolean => {
  const key = normalizeCity(city);
  return list.some((item) => normalizeCity(item) === key);
};

